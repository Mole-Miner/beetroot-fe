let variable = 123;
console.log(`= ${variable}`);

// variable = variable + 2;
variable += 2;
console.log(`+= ${variable}`);

// variable = variable - 4;
variable -= 4;
console.log(`-= ${variable}`);

// variable = variable * 3;
variable *= 3;
console.log(`*= ${variable}`);

// variable = variable / 5
variable /= 5;
console.log(`/= ${variable}`);

// variable = variable % 6
variable %= 6;
console.log(`%= ${variable}`);

// variable = variable ** 2;
variable **= 2;
console.log(`**= ${variable}`);
